    // Clean up widget areas
    function remove_widget_area($content) {
        unset($content['Index Top']);
        unset($content['Index Insert']);
        unset($content['Single Top']);
        unset($content['Single Insert']);
        unset($content['Page Top']);
        return $content;
    }
