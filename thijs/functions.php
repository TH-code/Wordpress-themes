    // Renaming widget areas
    function rename_widget_area($content) {
        
        $content['Primary Aside']['args']['name']   = '1st Right Column';
        $content['Secondary Aside']['args']['name'] = '2nd Right Column';
        
        $content['1st Subsidiary Aside']['args']['name'] = '1st Subcontent Block';
